package scraper.utils;

/**
 * Represents a type of source.
 *
 * @author Vetle Jahr
 * @version 1.0
 */
public enum SourceType {
    /**
     * Represents a File source.
     */
    FILE,
    /**
     * Represents a URL source.
     */
    URL,
    /**
     * Represents a String source.
     */
    STRING
}
